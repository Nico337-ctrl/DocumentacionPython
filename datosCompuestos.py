#listas

lista = ["Nicolas Ojeda", "Tengo 17 años"] #una lista se distingue por los "[]"
print(lista[0])
#las listas permiten tener datos repetidos
#permiten cambiar datos por indice


#tuplas

tupla = ("pato", "perro") #una tupla no se puede modificar 
print(tupla[0])
#las tuplas permiten tener datos repetidos
#permite modificar la tupla entera, mas no por indice



#sets ó conjuntos 

conjunto = {"gato", "fisica"} #los conjuntos no tienen orden fijo, pueden cambiar de posicion los datos que sigue funcionando
#no se pueden acceder a datos del conjunto por indice, se puede acceder a todo el conjunto
#tampoco se pueden realizar cambios a los datos dentor del conjunto, se puede cambiar el conjunto entero
#los conjuntos no permiten tener datos repetidos


#diccionario 

diccionario = {
    'nombre' : "Nicolas",
    'edad' : "17",
    'estado' : "no lo se"
}
print(diccionario["nombre"])
#su estructura es clave : valor y separar con ","



